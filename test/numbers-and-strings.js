
const tap = require('tap')
const depictsWholeNumber = require('../index.js')

tap.equal(depictsWholeNumber(0), true, '0 should be true')
tap.equal(depictsWholeNumber(123), true, '123 should be true')
tap.equal(depictsWholeNumber(-123), false, '-123 should be false')
tap.equal(depictsWholeNumber(1.23), false, '1.23 should be false')
tap.equal(depictsWholeNumber(2147483647), true, '2147483647 should be true')
tap.equal(depictsWholeNumber(Number.MAX_SAFE_INTEGER), true, 'Number.MAX_SAFE_INTEGER should be true')
tap.equal(depictsWholeNumber(Number.MAX_SAFE_INTEGER + 1), true, 'Number.MAX_SAFE_INTEGER + 1 should be true')
tap.equal(depictsWholeNumber(1e+21), false, '1e+21 should be false')
tap.equal(depictsWholeNumber(Number.MAX_VALUE), false, 'Number.MAX_VALUE should be false')
tap.equal(depictsWholeNumber(Infinity), false, 'Infinity should be false')
tap.equal(depictsWholeNumber(-Infinity), false, '-Infinity should be false')

tap.equal(depictsWholeNumber('0'), true, '"0" should be true')
tap.equal(depictsWholeNumber('123'), true, '"123" should be true')
tap.equal(depictsWholeNumber('-123'), false, '"-123" should be false')
tap.equal(depictsWholeNumber('1.23'), false, '"1.23" should be false')
tap.equal(depictsWholeNumber('2147483647'), true, '"2147483647" should be true')
tap.equal(depictsWholeNumber('9007199254740991'), true, '"9007199254740991" should be true')
tap.equal(depictsWholeNumber('9007199254740992'), true, '"9007199254740992" should be true')
tap.equal(depictsWholeNumber('1e3'), false, '"1e3" should be false')
tap.equal(depictsWholeNumber('1e+3'), false, '"1e+3" should be false')
tap.equal(depictsWholeNumber('0123'), false, 'String consisting of only numbers starting with zero should be false')
tap.equal(depictsWholeNumber('123s45'), false, 'String starting with an integer and containing non-number chars should be false')
