
const tap = require('tap')
const depictsWholeNumber = require('../index.js')

tap.equal(depictsWholeNumber(0), true)
tap.equal(depictsWholeNumber(1), true)
tap.equal(depictsWholeNumber(-1), false)
tap.equal(depictsWholeNumber(1.2), false)
tap.equal(depictsWholeNumber(-1.2), false)
tap.equal(depictsWholeNumber(2147483647), true)
tap.equal(depictsWholeNumber(-2147483648), false)
tap.equal(depictsWholeNumber(Number.MAX_SAFE_INTEGER), true)
tap.equal(depictsWholeNumber(Number.MIN_SAFE_INTEGER), false)
tap.equal(depictsWholeNumber(1e+21), false)
tap.equal(depictsWholeNumber(-1e+21), false)

tap.equal(depictsWholeNumber('0'), true)
tap.equal(depictsWholeNumber('01'), false)
tap.equal(depictsWholeNumber('1'), true)
tap.equal(depictsWholeNumber('-1'), false)
tap.equal(depictsWholeNumber('1.2'), false)
tap.equal(depictsWholeNumber('-1.2'), false)
tap.equal(depictsWholeNumber('2147483647'), true)
tap.equal(depictsWholeNumber('-2147483648'), false)
tap.equal(depictsWholeNumber('9007199254740991'), true)
tap.equal(depictsWholeNumber('-9007199254740991'), false)
tap.equal(depictsWholeNumber('1e+3'), false)
tap.equal(depictsWholeNumber('-1e+3'), false)
tap.equal(depictsWholeNumber('1e+21'), false)
tap.equal(depictsWholeNumber('-1e+21'), false)
tap.equal(depictsWholeNumber('string'), false)
